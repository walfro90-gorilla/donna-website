// components/auth/LoginForm.tsx
'use client';

import { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { useAuth } from '@/lib/auth/context';
import { AuthService } from '@/lib/auth/service';

interface FormState {
  email: string;
  password: string;
  isLoading: boolean;
  error: string | null;
}

export default function LoginForm() {
  const router = useRouter();
  const { signIn, user, loading } = useAuth();

  // Redirigir si ya hay sesi√≥n
  useEffect(() => {
    if (!loading && user) {
      const redirectPath = AuthService.getRedirectPath(user.role);
      console.log('üîê LoginForm: Usuario ya autenticado, redirigiendo a:', redirectPath);
      router.push(redirectPath);
    }
  }, [user, loading, router]);

  const [formState, setFormState] = useState<FormState>({
    email: '',
    password: '',
    isLoading: false,
    error: null,
  });

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();

    if (!formState.email || !formState.password) {
      setFormState(prev => ({
        ...prev,
        error: 'Por favor completa todos los campos',
      }));
      return;
    }

    setFormState(prev => ({ ...prev, isLoading: true, error: null }));

    try {
      const result = await signIn({
        email: formState.email,
        password: formState.password,
      });

      if (result.success && result.user) {
        const redirectPath = AuthService.getRedirectPath(result.user.role);
        console.log('üîê LoginForm: Redirigiendo a:', redirectPath);
        router.push(redirectPath);
      } else {
        setFormState(prev => ({
          ...prev,
          isLoading: false,
          error: result.error || 'Error de autenticaci√≥n',
        }));
      }
    } catch (error) {
      console.error('üîê LoginForm: Error inesperado:', error);
      setFormState(prev => ({
        ...prev,
        isLoading: false,
        error: 'Error inesperado. Intenta de nuevo.',
      }));
    }
  };

  const handleGoogleLogin = async () => {
    setFormState(prev => ({ ...prev, isLoading: true, error: null }));

    try {
      const result = await AuthService.signInWithGoogle();

      if (!result.success) {
        setFormState(prev => ({
          ...prev,
          isLoading: false,
          error: result.error || 'Error con Google Auth',
        }));
      }
      // Si es exitoso, se redirige autom√°ticamente a Google
    } catch (error) {
      console.error('üîê LoginForm: Error con Google:', error);
      setFormState(prev => ({
        ...prev,
        isLoading: false,
        error: 'Error inesperado con Google. Intenta de nuevo.',
      }));
    }
  };

  return (
    <div className="min-h-screen bg-gray-50 flex items-center justify-center py-12 px-4 sm:px-6 lg:px-8">
      <div className="max-w-md w-full space-y-8">
        <div>
          <h2 className="mt-6 text-center text-3xl font-extrabold text-gray-900">
            Iniciar Sesi√≥n
          </h2>
        </div>

        <form className="mt-8 space-y-6" onSubmit={handleSubmit}>
          {formState.error && (
            <div className="bg-red-50 border border-red-200 text-red-700 px-4 py-3 rounded">
              {formState.error}
            </div>
          )}

          {/* Mostrar errores de callback */}
          {typeof window !== 'undefined' && new URLSearchParams(window.location.search).get('error') && (
            <div className="bg-red-50 border border-red-200 text-red-700 px-4 py-3 rounded">
              {(() => {
                const error = new URLSearchParams(window.location.search).get('error');
                switch (error) {
                  case 'auth_error':
                    return 'Error en la autenticaci√≥n con Google. Intenta de nuevo.';
                  case 'profile_error':
                    return 'No se pudo crear tu perfil. Contacta al soporte.';
                  case 'unexpected_error':
                    return 'Error inesperado. Intenta de nuevo.';
                  default:
                    return 'Error en la autenticaci√≥n.';
                }
              })()}
            </div>
          )}

          <div className="space-y-4">
            <div>
              <label htmlFor="email" className="block text-sm font-medium text-gray-700">
                Email
              </label>
              <input
                id="email"
                name="email"
                type="email"
                required
                className="mt-1 appearance-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-md focus:outline-none focus:ring-[#e4007c] focus:border-[#e4007c] focus:z-10 sm:text-sm"
                placeholder="tu@email.com"
                value={formState.email}
                onChange={(e) => setFormState(prev => ({ ...prev, email: e.target.value }))}
                disabled={formState.isLoading}
              />
            </div>

            <div>
              <label htmlFor="password" className="block text-sm font-medium text-gray-700">
                Contrase√±a
              </label>
              <input
                id="password"
                name="password"
                type="password"
                required
                className="mt-1 appearance-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-md focus:outline-none focus:ring-[#e4007c] focus:border-[#e4007c] focus:z-10 sm:text-sm"
                placeholder="‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢"
                value={formState.password}
                onChange={(e) => setFormState(prev => ({ ...prev, password: e.target.value }))}
                disabled={formState.isLoading}
              />
            </div>
          </div>

          <div className="space-y-3">
            <button
              type="submit"
              disabled={formState.isLoading}
              className="group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-[#e4007c] hover:bg-[#c0006a] focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-[#e4007c] disabled:opacity-50 disabled:cursor-not-allowed"
            >
              {formState.isLoading ? (
                <div className="flex items-center">
                  <div className="w-4 h-4 border-2 border-white border-t-transparent rounded-full animate-spin mr-2"></div>
                  Iniciando sesi√≥n...
                </div>
              ) : (
                'Iniciar Sesi√≥n'
              )}
            </button>

            <div className="relative">
              <div className="absolute inset-0 flex items-center">
                <div className="w-full border-t border-gray-300" />
              </div>
              <div className="relative flex justify-center text-sm">
                <span className="px-2 bg-gray-50 text-gray-500">O contin√∫a con</span>
              </div>
            </div>

            <button
              type="button"
              onClick={handleGoogleLogin}
              disabled={formState.isLoading}
              className="group relative w-full flex justify-center py-2 px-4 border border-gray-300 text-sm font-medium rounded-md text-gray-700 bg-white hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-[#e4007c] disabled:opacity-50 disabled:cursor-not-allowed"
            >
              <svg className="w-5 h-5 mr-2" viewBox="0 0 24 24">
                <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
              </svg>
              Continuar con Google
            </button>
          </div>
        </form>
      </div>
    </div>
  );
}